
namespace Blazor_Formularios
{
   
    /*
       * Tratamento do Submit:
        
        - OnSubmit :
            Ocorre quando postamos o formulario enviando os dados para o servidor (permite enviar sempre).

        - OnValidSubmit :
            Ocorre quando o usuário posta um formulario válido. (deve ser usado com o componente DataAnnotationsValidator).

        - OnInvalidSubmit :
            Ocorre quando o usuário tentou postar um formulario que não respeita as regras de validação.

        * Cada um destes eventos passa um EditContext como um parâmetro, que podemos usar para determinar 
          o status da entrada do usuário.
     */



}

     * Local Storage : O local Storage permite amazenar dados de forma simples e sem expiração, 
             ou seja os dados ficam armazenados enquanto não forem apagados quer seja via código ou pelo 
             próprio navegador.


        * Session Storage: é muito parecido com o localStorage com a diferença de que no session Storage 
            os dados são limpos ao expirar a sessão da página, ou seja, quando a página (aba ou janela) 
            for fechada.        

        **Comando para instalar o Pacote => Install-Package Blazored.LocalStorage
--------------------------------------------------------------------------------------------------------------------------
                 
                 Não armazenar dados sensíveis ou críticos nestes locais. Uma boa pratica é armazenar
                 dados cifrados e para isso podemos usar os recursos do Asp.Net para proteço de dados

